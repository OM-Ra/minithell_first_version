#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;
	size_t len;

	j = ft_strlen(src);
	i = ft_strlen(dst);
	len = i;
	if (!size)
		return (j);
	if (i >= size)
		return (j + size);
	while (i < size - 1 && *src)
	{
		dst[i] = *src;
		src++;
		i++;
	}
	dst[i] = '\0';
	return (len + j);
}
