#include "libft.h"

size_t	ft_space_end(char const *s)
{
	int	i;

	i = ft_strlen(s) - 1;
	while (i >= 0 && (s[i] == ' ' || s[i] == '\n' || s[i] == '\t'))
	{
		if (i - 1 < 0)
			break ;
		i--;
	}
	return ((size_t)i);
}
